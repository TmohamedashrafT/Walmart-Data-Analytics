CREATE TABLE landing.customer (
    customer_id INT,
    customer_first_name VARCHAR(50),
    customer_last_name VARCHAR(50),
    customer_age INT,
    customer_email VARCHAR(100),
    created_by VARCHAR(50),
    modified_by VARCHAR(50),
    created_at TIMESTAMP,
    modified_at TIMESTAMP,
    phone VARCHAR(20),
    city_id INT
);
